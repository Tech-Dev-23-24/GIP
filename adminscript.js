  function showDiv() {
    const div = document.getElementById("sidenav");
    const nav = document.getElementById("top");



        
        if (!div.classList.contains("hidden") && !div.classList.contains("visible")) {
            // If the div has no class, add the "visible" class by default
            div.classList.add("visible");
          
          } else if (div.classList.contains("hidden")) {
            div.classList.remove("hidden");
            nav.classList.remove("margins");
            div.classList.add("visible");
          } else {
            div.classList.remove("visible");
            nav.classList.add("margins");
            div.classList.add("hidden");
          }
        
  
    }

